# integer (int)
# tipe data : angka satuan yang tidak punya koam
data_integer = 11
print('data : ', data_integer)
# type() berfungsi untuk mengetahui tipe data variabel
print('tpe data :',type (data_integer)) # <class 'int'>

# float
# tipe data : angka dengan koma (koma = titik)
data_float = 3.14
print ('data : ', data_float)
print ('tipe data ; ', type (data_float))

#string
#tipe data : kumpulan karakter
data_srtring = 'undima'
print ('data : ', data_srtring)
print ('tipe data ; ', type (data_srtring))

#boolean
#tipe data : true/false

data_boolean = False
print('data : ', data_boolean)
print ('tipe data ; ', type (data_boolean))

